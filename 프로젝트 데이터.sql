-- 데이터베이스 생성
DROP DATABASE IF EXISTS PROJECT ;
CREATE DATABASE IF NOT EXISTS PROJECT ;

USE PROJECT;

CREATE TABLE regional_birth_rate (
    region_id INT NOT NULL,            -- 지역 ID
    region_name VARCHAR(100) NOT NULL, -- 지역 이름
    year INT NOT NULL,                 -- 연도
    birth_rate FLOAT NOT NULL,         -- 출산율
    PRIMARY KEY (region_id, year)      -- 복합 기본 키: 지역 ID와 연도
);

CREATE TABLE age_pet_ownership (
    age_group VARCHAR(20) NOT NULL,    -- 연령대 (예: '20-29', '30-39')
    year INT NOT NULL,                 -- 연도
    region_id INT NOT NULL,            -- 지역 ID
    ownership_rate FLOAT NOT NULL,     -- 반려동물 소유율 (%)
    PRIMARY KEY (age_group, year, region_id), -- 복합 기본 키
    FOREIGN KEY (region_id) REFERENCES regional_birth_rate(region_id) -- 지역 ID 참조
);

CREATE TABLE pet_expenses (
    expense_id INT AUTO_INCREMENT PRIMARY KEY, -- 소비 데이터 고유 ID
    region_id INT NOT NULL,                    -- 지역 ID
    year INT NOT NULL,                         -- 연도
    expense_type VARCHAR(50) NOT NULL,         -- 소비 유형 (예: 'medical', 'supplies', 'food')
    expense_amount FLOAT NOT NULL,             -- 소비 금액
    FOREIGN KEY (region_id) REFERENCES regional_birth_rate(region_id) -- 지역 ID 참조
);

CREATE TABLE elder_population (
    region_id INT NOT NULL,        -- 지역 ID
    year INT NOT NULL,             -- 연도
    elder_ratio FLOAT NOT NULL,    -- 노인 비율 (%)
    PRIMARY KEY (region_id, year), -- 복합 기본 키: 지역 ID와 연도
    FOREIGN KEY (region_id) REFERENCES regional_birth_rate(region_id) -- 지역 ID 참조
);

